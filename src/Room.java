public class Room {
    private String name;

    private String north;
    private String east;
    private String south;
    private String west;

    // Constructor
    Room(String name, String north, String east, String south, String west){
        this.name = name;

        this.north = north;
        this.east = east;
        this.south = south;
        this.west = west;
    }






}
